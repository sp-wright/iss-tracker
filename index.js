function getISS() {
  $.getJSON("http://api.open-notify.org/iss-now.json?callback=?", function (data) {
    let latitude = Number(data["iss_position"]["latitude"]);
    let longitude = Number(data["iss_position"]["longitude"]);
    initMap({ coords: { lat: latitude, lng: longitude } });
  });
  setTimeout(getISS, 60000);
}
